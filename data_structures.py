# Lists
my_list = [1, 2, 3, 4, 5]
print(my_list)

# Tuples
my_tuple = (2, 1, 3, 4, 5)
print(my_tuple)

# Dictionaries
my_dict = {'apple': 3, 'banana': 2, 'orange': 5}
print(my_dict)

# Sets
my_set = {4, 5, 1, 3, 2}
print(my_set)
print("")

# Iterating over a list
for item in my_list:
    print(item)

# Iterating over a dictionary
for key, value in my_dict.items():
    print(key, value)