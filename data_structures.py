# Lists
my_list = [1, 2, 3, 4, 5, 3]
print(my_list)
my_list.append(6)
print(my_list)
my_list.remove(3)
print(my_list)
print("")

# Tuples
my_tuple = (2, 1, 3, 2, 4, 5)
print(my_tuple)
print("")

# Sets
my_set = {4, 5, 1, 3, 2}
print(my_set)
print("")

# Dictionaries
my_dict = {'banana': 2, 'orange': 5, 'apple': 3}
print(my_dict)
print("")

# Iterating over a list
for item in my_list:
    print(item)

# Iterating over a dictionary
for key, value in my_dict.items():
    print(key, value)